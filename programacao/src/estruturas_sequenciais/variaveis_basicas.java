package estruturas_sequenciais;

public class variaveis_basicas {
	
	/*
	 
	 VAR:
	 
	 Definição informal: uma porção de memória(RAM) utilizada para armazenar dados durante a execução dos programas.
	 
	 Sintaxe: <tipo> <nome> = <valor inicial>(opcional)
	 
	 Exemplos:
	 	int idade = 25; (Numero inteiro)
	 	double altura = 1.68; (Numero flutuante com casas decimais)
	 	char sexo = 'F' (char representa um caracter)
	 
	 Uma variável possui:
	 	°Nome (ou identificador)
	 	°Tipo
	 	°Valor
	 	°Endereço (Significa em qual local da memoria ram a var está)
	 	
	 TIPOS PRIMITIVOS EM JAVA:
	 
	 Descrição:
		
	 tipos numericos inteiros: byte(8); short(16); int(32); long(64); (valor padrão = 0)
	 
	 tipos numéricos com ponto flutunte: float(32); double(64);(valor padrão = 0.0F e 0.0)
	 
	 um caracter Unicode: char(16); (valor padrão = '\u0000')
	 
	 valor verdade: boolean(1); (valor padrão: false) (OBS: TRUE OR FALSE)
	 
	 String: cadeia de caracteres(Palavras ou textos);
	 
	 */

}
